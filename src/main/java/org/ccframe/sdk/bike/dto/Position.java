package org.ccframe.sdk.bike.dto;

public class Position {
	private Double lng;
	private Double lat;
	
	public Position(){
		
	}
	
	public Double getLng() {
		return lng;
	}
	public void setLng(Double lng) {
		this.lng = lng;
	}
	public Double getLat() {
		return lat;
	}
	public void setLat(Double lat) {
		this.lat = lat;
	}

	
}
